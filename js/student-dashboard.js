$(function () {

    'use strict';

    (function () {

        let middle = document.querySelector('#block-middle');

        if ($(window).width() <= 767) {
            middle.classList.remove('block-middle');

        }
        $(window).on('resize', function () {

            if ($(window).width() > 767) {
                middle.classList.add('block-middle');

            } else {
                middle.classList.remove('block-middle');
            }

        });

    }());

});